“bobbers” yang berasal dari kata “bobbed” yang berarti melepaskan sebagian besar beban dari motor, pada dasarnya adalah melepas atau memotong fender depan belakang yang bertujuan untuk membuat motor menjadi ringan dan motor dapat melaju lebih cepat dan terlihat lebih simple.

Selain dari pada itu penampilan motor itu menjadi lebih unik dan enak dipandang mata.aliran bobbed ini juga adalah asal mula dari aliran minimalist rider contoh pendahulu bobber yaitu 1940 indian sport bob-job yang telah di pamerkan pada pertunjukan “the art of the motorcycle exhibitions” indian scouts dan indian chiefs yang hadir pada masa nya dengan tampilan fenders yang sangat besar depan dan belakang yang hampir menutupi keseluruhan roda.begitu pula pada luxurius 1941 indian series 441, sementara itu motor racing hanya menggunakan fenders kecil dan bahkan ada yang tanpa fenders sama sekali.

ukuran motor yang besar dengan kelengkapan full covers menjadi inspirasi para builder otomotif di zaman itu untuk menjadikan bobber sebagai bahan seni yang indah untuk di modifikasi.

dari Para builder bobbed menemukan inspirasi untuk generasi selanjutnya yaitu aliran cafe racers dan choppers,dan bermula dari situ banyak inspirasi baru yang berkembang dan aliran aliran baru sampai saat ini.
